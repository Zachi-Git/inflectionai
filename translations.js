const LANG_META = {
  en: { flag: '🇺🇸', label: 'English' },
  zh: { flag: '🇨🇳', label: '中文' },
  ko: { flag: '🇰🇷', label: '한국어' },
  ja: { flag: '🇯🇵', label: '日本語' },
  es: { flag: '🇪🇸', label: 'Español' },
};

window.i18n = {
  lang: 'en',

  t(key) {
    return (TRANSLATIONS[this.lang] || TRANSLATIONS.en)[key]
        || TRANSLATIONS.en[key]
        || key;
  },

  setLang(lang) {
    if (!TRANSLATIONS[lang]) return;
    this.lang = lang;
    localStorage.setItem('iai_lang', lang);
    this.apply();
    document.documentElement.lang = lang;
  },

  apply() {
    const t = (key) => this.t(key);
    // Text nodes via data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const v = t(el.dataset.i18n);
      if (v) el.textContent = v;
    });
    // Placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const v = t(el.dataset.i18nPh);
      if (v) el.placeholder = v;
    });
    // Update language switcher active state
    document.querySelectorAll('.lang-opt').forEach(el => {
      el.classList.toggle('active', el.dataset.lang === this.lang);
    });
  },

  init() {
    const saved = localStorage.getItem('iai_lang');
    const browser = navigator.language?.slice(0, 2);
    const map = { zh: 'zh', ko: 'ko', ja: 'ja', es: 'es' };
    this.lang = saved || map[browser] || 'en';
    this.apply();
  },
};
